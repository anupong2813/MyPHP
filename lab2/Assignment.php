<?php
echo "<h3>PHP Assignment Operators</h3>";

// กำหนดค่าเริ่มต้นใหม่
$c = 500;
echo "c = $c <br>";

// 1. Addition Assignment (+=)
$c = 500; // Reset ค่ากลับเป็น 500
$c += 120;
echo "c+ = 120 = $c <br>";

// 2. Subtraction Assignment (-=)
$c = 500; // Reset ค่ากลับเป็น 500
$c -= 250;
echo "c- = 250 = $c <br>";

// 3. Multiplication Assignment (*=)
$c = 500; // Reset ค่ากลับเป็น 500
$c *= 3;
echo "c* = 3 = $c <br>";

// 4. Division Assignment (/=)
$c = 500; // Reset ค่ากลับเป็น 500
$c /= 60;
echo "c/ = 60 = $c <br>";

// 5. Modulus Assignment (%=)
$c = 500; // Reset ค่ากลับเป็น 500
$c %= 70;
echo "c% = 70 = $c <br>";
?>